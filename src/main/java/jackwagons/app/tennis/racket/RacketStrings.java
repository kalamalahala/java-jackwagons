package jackwagons.app.tennis.racket;

public class RacketStrings implements RacketStringsInterface {

    private String mainStringMaterial;
    private String mainStringManufacturer;
    private String mainStringColor;
    private String mainStringGauge;
    private int mainStringTension;
    private String crossStringMaterial;
    private String crossStringManufacturer;
    private String crossStringColor;
    private String crossStringGauge;
    private int crossStringTension;
    public RacketStrings() {
    }

    public String getCrossStringManufacturer() {
        return crossStringManufacturer;
    }

    public void setCrossStringManufacturer(String crossStringManufacturer) {
        this.crossStringManufacturer = crossStringManufacturer;
    }

    public String getMainStringMaterial() {
        return mainStringMaterial;
    }

    public void setMainStringMaterial(String mainStringMaterial) {
        this.mainStringMaterial = mainStringMaterial;
    }

    public String getMainStringManufacturer() {
        return mainStringManufacturer;
    }

    public void setMainStringManufacturer(String mainStringManufacturer) {
        this.mainStringManufacturer = mainStringManufacturer;
    }

    public String getMainStringColor() {
        return mainStringColor;
    }

    public void setMainStringColor(String mainStringColor) {
        this.mainStringColor = mainStringColor;
    }

    public String getMainStringGauge() {
        return mainStringGauge;
    }

    public void setMainStringGauge(String mainStringGauge) {
        this.mainStringGauge = mainStringGauge;
    }

    public int getMainStringTension() {
        return mainStringTension;
    }

    public void setMainStringTension(int mainStringTension) {
        this.mainStringTension = mainStringTension;
    }

    public String getCrossStringMaterial() {
        return crossStringMaterial;
    }

    public void setCrossStringMaterial(String crossStringMaterial) {
        this.crossStringMaterial = crossStringMaterial;
    }

    public String getCrossStringColor() {
        return crossStringColor;
    }

    public void setCrossStringColor(String crossStringColor) {
        this.crossStringColor = crossStringColor;
    }

    public String getCrossStringGauge() {
        return crossStringGauge;
    }

    public void setCrossStringGauge(String crossStringGauge) {
        this.crossStringGauge = crossStringGauge;
    }

    public int getCrossStringTension() {
        return crossStringTension;
    }

    public void setCrossStringTension(int crossStringTension) {
        this.crossStringTension = crossStringTension;
    }


}
